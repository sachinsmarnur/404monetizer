export function generateThemeCSS(theme: ThemeType, mode: 'light' | 'dark' = 'light'): string {
  const config = themeConfigs[theme];
  const vars = config.cssVars[mode];
  
  const cssVarDeclarations = Object.entries(vars)
    .map(([key, value]) => `    --${key}: ${value};`)
    .join('\n');

  return `
  :root {
${cssVarDeclarations}
  }
`;
}

export function getThemePreviewColors(theme: ThemeType) {
  const config = themeConfigs[theme];
  const lightVars = config.cssVars.light;
  
  return {
    primary: `hsl(${lightVars.primary})`,
    background: `hsl(${lightVars.background})`,
    foreground: `hsl(${lightVars.foreground})`,
    muted: `hsl(${lightVars.muted})`,
    accent: `hsl(${lightVars.accent})`,
  };
} 